# https://pyrosm.readthedocs.io/en/latest/basics.html#filtering-data-based-on-bounding-box
# Pyrosm makes it easy to extract various datasets from OpenStreetMap pbf-dumps including e.g. road networks, buildings,
# Points of Interest (POI), landuse and natural elements. Also fully customized queries are supported which makes it possible
# to parse the data from OSM with more specific filters.

# Pyrosm is easy to use and it provides a somewhat similar user interface as OSMnx. The main difference between pyrosm and OSMnx is that
# OSMnx reads the data over internet using OverPass API, whereas pyrosm reads the data from local OSM data dumps that can be
# downloaded e.g. from GeoFabrik's website. This makes it possible to read data faster thus allowing e.g. parsing street networks
# for the whole country fairly efficiently (however, see caveats).



